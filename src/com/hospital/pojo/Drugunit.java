package com.hospital.pojo;

public class Drugunit {
    private Integer drugunitid;

    private String drugunitname;

    public Integer getDrugunitid() {
        return drugunitid;
    }

    public void setDrugunitid(Integer drugunitid) {
        this.drugunitid = drugunitid;
    }

    public String getDrugunitname() {
        return drugunitname;
    }

    public void setDrugunitname(String drugunitname) {
        this.drugunitname = drugunitname;
    }
}